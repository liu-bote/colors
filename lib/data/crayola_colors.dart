/// A Crayola crayon color. [name] is the official English name; localized
/// names for the learning languages come from [colorNameTranslations].
class CrayolaColor {
  final String name;
  final Color color;

  const CrayolaColor(this.name, this.color);

  /// Name in the given language code, falling back to English.
  String nameIn(String lang) =>
      lang == 'en' ? name : colorNameTranslations[lang]?[name] ?? name;
}

